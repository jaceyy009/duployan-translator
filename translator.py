# Helper to detect tense from English sentence (simple keyword check)
def detect_tense(words):
    if any(w in ['was', 'went', 'had', 'did', 'came', 'made', 'took', 'began'] for w in words):
        return 'past'
    elif any(w in ['will', 'would'] for w in words):
        return 'future'
    return 'current'

# English to Duployan
def translate_english_to_duployan(sentence):
    words = sentence.lower().replace('.', '').replace(',', '').split()
    tense = detect_tense(words)
    # Simple VSO: find verb, subject, object (assume first noun/pron subject, verb, rest object)
    subject = None
    verb = None
    object_ = []
    for w in words:
        if w in core_vocab:
            if not subject and w in ['i', 'you', 'he', 'she', 'we', 'they', 'it', 'him', 'us', 'me', 'her', 'them', 'person', 'people', 'man', 'woman', 'child', 'friend', 'girl', 'boy']:
                subject = w
            elif not verb and w in ['be', 'have', 'go', 'come', 'see', 'say', 'speak', 'learn', 'know', 'make', 'use', 'go', 'come', 'see', 'say', 'learn', 'know', 'make', 'use', 'write', 'like', 'do', 'will', 'would', 'find', 'take', 'get', 'give', 'tell', 'call', 'ask', 'change', 'need', 'try', 'build', 'stand', 'let', 'keep', 'start', 'run', 'hear', 'cut', 'watch', 'eat', 'feel', 'talk', 'sing', 'listen', 'carry', 'serve', 'appear', 'govern', 'pull', 'fall', 'lead']:
                verb = w
            else:
                object_.append(w)
    if not subject or not verb:
        return 'Unable to parse sentence (missing subject or verb)', ''

    subject_dup, subject_pron = core_vocab.get(subject, ('?', '?'))
    verb_dup, verb_pron = core_vocab.get(verb, ('?', '?'))
    object_dup = ' '.join(core_vocab.get(o, ('?', '?'))[0] for o in object_)
    object_pron = '-'.join(core_vocab.get(o, ('?', '?'))[1] for o in object_)

    tense_part, tense_pron = tense_particles[tense]

    # Build Duployan: tense + verb + subject + object
    dup_sentence = ' '.join(filter(None, [tense_part, verb_dup, subject_dup, object_dup]))
    pron_sentence = '-'.join(filter(None, [tense_pron, verb_pron, subject_pron, object_pron]))

    return dup_sentence, pron_sentence

# Duployan to English
def translate_duployan_to_english(dup_sentence):
    words = dup_sentence.split()
    if len(words) < 2:
        return 'Invalid Duployan sentence', ''

    tense_part = words[0]
    verb_dup = words[1]
    subject_dup = words[2]
    object_dup = ' '.join(words[3:])

    tense = next((k for k, v in tense_particles.items() if v[0] == tense_part), 'current')
    verb_eng = dup_to_eng.get(verb_dup, '?')
    subject_eng = dup_to_eng.get(subject_dup, '?')
    object_eng = ' '.join(dup_to_eng.get(o, '?') for o in object_dup.split())

    # SVO
    eng_sentence = f"{subject_eng} {tense} {verb_eng} {object_eng}".strip()

    return eng_sentence, ''  # No pronun for English

